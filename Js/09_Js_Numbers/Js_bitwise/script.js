// And &
console.log(5 & 1); 
//    5 --> 101
//    1 --> 001
//  total-->001 --> 1

// OR |
console.log(5|1); // 5;
//    5 --> 101
//    1 --> 001
//  total-->101 --> 5

// XOR ^ --> return1 if the bit are different
console.log(5^1); // 4
//    5 --> 101
//    1 --> 001
//  total-->100 --> 4

//NOT ~
console.log(~5); // -6

// left shift <<
console.log(5<<1); // 10
//    5 --> 0101
// 5<<1 --> 1010 (10)

// right shift >>
console.log(5>>1) // 2
//    5--->0101
// 5>>1--->0010 (2)